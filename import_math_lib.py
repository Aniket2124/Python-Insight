import math

val = 3.14
sqrtval = math.sqrt(val)
print(sqrtval)
print(sqrtval**(2))  # to cross verify sqrt
expval = math.exp(val)
print(expval)
factval = math.factorial(math.floor(val))
print(factval)
sinval = math.sin(val)
print(sinval)
floorval = math.floor(val)
print(floorval)
ceilingval = math.ceil(val)
print(ceilingval)
